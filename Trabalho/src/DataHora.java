public class DataHora {
    private int mes;
    private int dia;
    private int hora;

    public DataHora(int mes, int dia, int hora){
        this.mes = mes;
        this.dia = dia;
        this.hora = hora;
    }

    public int getMes() {
        return mes;
    }

    public int getDia() {
        return dia;
    }

    public int getHora() {
        return hora;
    }
}
